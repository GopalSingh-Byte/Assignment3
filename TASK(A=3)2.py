import math
#Input from user
num= float(input("Enter the number: "))
 
#Calculation using math module
sqrt_result = math.sqrt(num)
log_result = math.log(num) # natural log
sine_result = math.sin(num) #Input in radians

#Display results
print(f"square root:{sqrt_result}")
print(f"logarithm:{log_result}")
print(f"sine root:{sine_result}")
